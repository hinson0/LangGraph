from typing import Literal
from pydantic import BaseModel, Field
from langgraph.func import entrypoint, task
from langchain_openai import ChatOpenAI
from langgraph.checkpoint.memory import MemorySaver


class Feedback(BaseModel):
    status: Literal["ok", "nah"] = Field(description="判断这首诗是否满意")
    content: str = Field(description="如果不满意，那么请提供改进它的建议")


llm = ChatOpenAI(model="Qwen/Qwen3-8B")
evaluator = llm.with_structured_output(Feedback)


@task
def generate_a_poem(topic, feedback) -> str:
    if feedback:
        print(1)
        print(feedback)
        poem = llm.invoke(
            f"生成一个关于{topic}主题的诗,并参考反馈建议:{feedback.content}"
        )
    else:
        poem = llm.invoke(f"生成一个关于{topic}主题的诗")
    return poem.content  # type: ignore


@task
def evaluate_a_poem(poem: str) -> Feedback:
    feedback = evaluator.invoke(f"帮我评估一下这首诗{poem}")
    return feedback  # type: ignore


@entrypoint()
def workflow(topic):
    feedback, retry_counter = None, 0
    while True:
        poem = generate_a_poem(topic, feedback).result()
        print(poem)
        print("----")
        feedback = evaluate_a_poem(poem).result()
        print(feedback.status)
        print(feedback.content)
        if feedback.status == "ok":
            retry_counter += 1
        if retry_counter == 2:
            break
    return poem


result = workflow.invoke("冬天")
print(result)
